# Datatypes in Python
# Python has several built-in data types that are used to store different kinds of data.
# The most common data types are:
# 1. Integers: Whole numbers, e.g., 1, 2, 3
# 2. Floats: Decimal numbers, e.g., 1.5, 2.3, 3.14
# 3. Strings: Text data, e.g., "Hello", "World"
# 4. Booleans: True or False values
# 5. None: Represents the absence of a value

a=30  # An integer
b=71.22  # A float
c="Hello"  # A string
d=True  # A boolean
e=None  # None type, represents no value